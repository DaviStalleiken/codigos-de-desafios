alt = float(input('Digite a altura da parede: '))
lar = float(input('Digite a largura da parede: '))
area = alt * lar
totinta = area / 2
print(f'Considere que sua parede é de dimensão {alt}x{lar}, sua área é de {area}m².')
print(f'Portanto, você precisará de {totinta} litros de tinta para pintá-la.')
